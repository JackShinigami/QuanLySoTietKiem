GO

USE QL_STK
CREATE TABLE KHACH_HANG(
	CCCD CHAR(12), 
	HOTEN NVARCHAR(30), 
	DIACHI NVARCHAR(150)

	CONSTRAINT PK_KH
	PRIMARY KEY(CCCD)
)

CREATE TABLE SO_TIET_KIEM(
	MASO INT, 
	CCCD CHAR(12),
	NGAYMOSO DATE, 
	NGAYDONGSO DATE,
	SODU BIGINT, 
	LOAITIETKIEM INT, 
	TRANGTHAI INT, 
	LAISUAT FLOAT, 
	SONGAYDUOCRUT INT, 
	TIENGUITOITHIEU BIGINT

	CONSTRAINT PK_STK
	PRIMARY KEY(MASO)
)

CREATE TABLE PHIEU_GUI(
	MAPHIEUGUI INT, 
	MASO INT,
	NGAYGUI DATE, 
	SOTIEN BIGINT

	CONSTRAINT PK_PG
	PRIMARY KEY(MAPHIEUGUI)

)

CREATE TABLE PHIEU_RUT(
	MAPHIEURUT INT, 
	MASO INT, 
	NGAYRUT DATE, 
	SOTIEN BIGINT

	CONSTRAINT PK_PR
	PRIMARY KEY(MAPHIEURUT)
)

CREATE TABLE LOAI_TIET_KIEM(
	KYHAN INT, 
	LAISUAT FLOAT 

	CONSTRAINT PK_LTK
	PRIMARY KEY(KYHAN)
)

CREATE TABLE CONFIG_TOITHIEU(
	NGAYGUI INT, 
	SOTIENGUI BIGINT
)

ALTER TABLE SO_TIET_KIEM
